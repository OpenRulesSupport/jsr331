package org.jcp.jsr331.samples;
import javax.constraints.Objective;
import javax.constraints.Problem;
import javax.constraints.ProblemFactory;
import javax.constraints.SearchStrategy;
import javax.constraints.Solution;
import javax.constraints.Solver;
import javax.constraints.ValueSelectorType;
import javax.constraints.Var;

public class KnapsackProblemWithSelectors {

	public static void main(String[] argv){
		
		long executionStart = System.currentTimeMillis();
	    try{
	      int nbResources = 7;
	      int nbItems = 12;
	      int[] capacity= {18209, 7692, 1333, 924, 26638, 61188, 13360};
	      int[] value= {96, 76, 56, 11, 86, 10, 66, 86, 83, 12, 9, 81};
	      int[][] use = {
	       {19, 1, 10, 1, 1, 14, 152, 11, 1, 1, 1, 1},
	       {0, 4, 53, 0, 0, 80, 0, 4, 5, 0, 0, 0},
	       {4, 660, 3, 0, 30, 0, 3, 0, 4, 90, 0, 0},
	       {7, 0, 18, 6, 770, 330, 7, 0, 0, 6, 0, 0},
	       {0, 20, 0, 4, 52, 3, 0, 0, 0, 5, 4, 0},
	       {0, 0, 40, 70, 4, 63, 0, 0, 60, 0, 4, 0},
	       {0, 32, 0, 0, 0, 5, 0, 3, 0, 660, 0, 9}
	      };

	      Problem p = ProblemFactory.newProblem("KnapsackProblemWithSelectors");
	      // variables
	      Var[] take = p.variableArray("take", 0, 61188, nbItems);
	      
	      // costFunction to maximize
	      Var costFunc = p.scalProd(value,take);
	      p.add("costFunc", costFunc);
	      
	      // capacity due constraints
	      for (int r=0;r<nbResources;r++){
	    	  p.post( p.scalProd(use[r],take),"<=",capacity[r]);
	      }

	      p.log("Posted constraints in " + (System.currentTimeMillis()-executionStart) + " msec");
	      p.log(take);
	      Solver solver = p.getSolver();
	      SearchStrategy strategy = solver.getSearchStrategy();
	      strategy.setValueSelectorType(ValueSelectorType.MAX);
	      strategy.setVars(take);
	      solver.traceSolutions(true);
	      Solution solution = solver.findOptimalSolution(Objective.MAXIMIZE, costFunc);
	      //Solution solution = solver.findSolution();
	      if (solution == null){
	         p.log("The problem has no optimal solution!");
	         return;
	      }
	      else {
		      p.log("Optimized cost function value: " + solution.getValue("costFunc") );
		      solution.log();
		      //p.log("Cost: " + costFunc.getValue());
	      }
	      long executionTime = System.currentTimeMillis() - executionStart;
	      p.log("Total Execution time: " + executionTime + " msec");
//	      p.log("Variables: ");
//	      Var[] vars = solution.getVars();
//	      for (int i=0;i<take.length;i++){
//	     	  p.log( vars[i].getName() + " = " + vars[i].getValue() );
//	      }
//	      Var obj = (Var)solution.getObjective();
//	      p.log( obj.getName()+  " = " + obj.getValue() );
	    }
	    catch(Exception e){
	      e.printStackTrace();
	    }
	  }
}

